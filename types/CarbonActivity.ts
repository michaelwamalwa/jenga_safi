// Central enum for activity types
export enum CarbonActivityType {
    ENERGY = "energy",          // Grid or diesel use
    RENEWABLE = "renewable",    // Solar, wind, etc.
    TRANSPORT = "transport",    // Fuel for vehicles/logistics
    MACHINERY = "machinery",    // Diesel for machinery
    MATERIAL = "material",      // Sustainable vs. standard materials
    WASTE = "waste",            // Waste generation
    RECYCLING = "recycling",    // Recycling efforts
    WATER = "water",            // Water consumption
    WATER_REUSE = "waterReuse", // Water reuse/recycling savings
  }
  